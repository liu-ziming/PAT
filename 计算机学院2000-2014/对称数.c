
/*
1-打印所有不超过 n（n<256）的，其平方具有对称 性质的数。如 11*11=121。 
*/


#include <stdio.h>

int main()
{
	int n;
	
	for ( n = 4; n < 256; ++n)  //4开始平方开始为两位数
	{
		/* 遍历所有数 */
		int s1=0,s2=0; //注意每次都要初始化 
		s1=n*n;
		while(s1)
		{
			s2 = s2*10+s1%10;      //得到反序数s2
			s1/=10;
		}
         if (s2==n*n)
         {
         	printf("%d\n",n );
         }

	}
}
